     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function range(Request $request)
    {
        $request->validate([
            "start" => ['required'],
            "end" => ['required'],
        ]);

        $order = Order::with(['shipping', 'user'])->whereBetween('updated_at', [$request->start, $request->end])->get();

        return redirect()->back()->with([
            'report' => $order,
        ]);
    }

